/* -----------------------------
   AIR LAYER
------------------------------ */

interface AirParticle {
  id: number;
  x: number;
  y: number;
  vx: number;
  vy: number;
  life: number;
  large: boolean;
  pollutant: Pollutant;
}

function AirLayer({
  active,
  pollutants,
  resetKey,
}: {
  active: boolean;
  pollutants: Pollutant[];
  resetKey: number;
}) {
  const [particles, setParticles] = useState<AirParticle[]>([]);
  const [hovered, setHovered] = useState<number | null>(null);

  // Clear particles when site changes
  useEffect(() => {
    setParticles([]);
  }, [resetKey]);

  useEffect(() => {
    if (!active) return;

    const interval = setInterval(() => {
      setParticles((prev) => {
        const updated = prev
          .map((p) => ({
            ...p,
            x: p.x + p.vx,
            y: p.y + p.vy,
            life: p.life - (p.large ? 0.005 : 0.015),
          }))
          .filter((p) => p.life > 0);

        const pollutant =
          pollutants[Math.floor(Math.random() * pollutants.length)];

        const large = Math.random() < 0.05;

        updated.push({
          id: Date.now() + Math.random(),
          x: 50 + (Math.random() - 0.5) * 8,
          y: 52,
          vx: (Math.random() - 0.5) * 0.8,
          vy: (Math.random() - 0.8) * 0.7,
          life: 1.2,
          large,
          pollutant,
        });

        return updated;
      });
    }, 90);

    return () => clearInterval(interval);
  }, [active, pollutants]);

  return (
    <>
      {particles.map((p) => {
        const isHovered = hovered === p.id;

        return (
          <div
            key={p.id}
            className="absolute"
            style={{
              left: `${p.x}%`,
              top: `${p.y}%`,
              transform: "translate(-50%, -50%)",
              zIndex: isHovered ? 100 : 20,
            }}
            onMouseEnter={() => p.large && setHovered(p.id)}
            onMouseLeave={() => setHovered(null)}
          >
            {p.large ? (
              <div className="relative">
                {isHovered && <Tooltip pollutant={p.pollutant} />}

                <div
                  className="w-20 h-20 rounded-full flex items-center justify-center text-lg distressed-text-normal font-black shadow-xl"
                  style={{
                    background: p.pollutant.color,
                    opacity: p.life,
                  }}
                >
                  {p.pollutant.code}
                </div>
              </div>
            ) : (
              <div
                className="rounded-full blur-[1px]"
                style={{
                  width: 12,
                  height: 12,
                  background: p.pollutant.color,
                  opacity: p.life * 0.7,
                }}
              />
            )}
          </div>
        );
      })}
    </>
  );
}

/* -----------------------------
   SOIL LAYER
------------------------------ */

interface SoilBlob {
  id: number;
  x: number;
  y: number;
  size: number;
  pollutant: Pollutant;
}

function SoilLayer({
  active,
  pollutants,
  resetKey,
}: {
  active: boolean;
  pollutants: Pollutant[];
  resetKey: number;
}) {
  const [blobs, setBlobs] = useState<SoilBlob[]>([]);
  const [hovered, setHovered] = useState<number | null>(null);

  // Clear blobs when site changes
  useEffect(() => {
    setBlobs([]);
  }, [resetKey]);

  useEffect(() => {
    if (!active) return;

    const interval = setInterval(() => {
      const pollutant =
        pollutants[Math.floor(Math.random() * pollutants.length)];

      setBlobs((prev) => [
        ...prev,
        {
          id: Date.now() + Math.random(),
          x: 10 + Math.random() * 80, // spread across full width
          y: 70 + Math.random() * 10,
          size: 60 + Math.random() * 60,
          pollutant,
        },
      ]);
    }, 1800); // slower generation

    return () => clearInterval(interval);
  }, [active, pollutants]);

  return (
    <>
      {blobs.map((blob) => {
        const isHovered = hovered === blob.id;

        return (
          <div
            key={blob.id}
            className="absolute"
            style={{
              left: `${blob.x}%`,
              top: `${blob.y}%`,
              transform: "translate(-50%, -50%)",
              zIndex: isHovered ? 120 : 5,
            }}
            onMouseEnter={() => setHovered(blob.id)}
            onMouseLeave={() => setHovered(null)}
          >
            {isHovered && <Tooltip pollutant={blob.pollutant} />}

            <div
              className="rounded-full animate-pulse cursor-pointer flex items-center justify-center text-white font-black text-sm distressed-text-normal"
              style={{
                width: blob.size,
                height: blob.size,
                background: blob.pollutant.color,
                opacity: 0.78,
              }}
            >
              {blob.pollutant.code}
            </div>
          </div>
        );
      })}
    </>
  );
}

/* -----------------------------
   WATER LAYER
------------------------------ */

interface WaterStream {
  id: number;
  x: number;
  y: number;
  width: number;
  pollutant: Pollutant;
}

function WaterLayer({
  active,
  pollutants,
  resetKey,
}: {
  active: boolean;
  pollutants: Pollutant[];
  resetKey: number;
}) {
  const [streams, setStreams] = useState<WaterStream[]>([]);
  const [hovered, setHovered] = useState<number | null>(null);

  // Clear streams when site changes
  useEffect(() => {
    setStreams([]);
  }, [resetKey]);

  useEffect(() => {
    if (!active) return;

    const interval = setInterval(() => {
      const pollutant =
        pollutants[Math.floor(Math.random() * pollutants.length)];

      setStreams((prev) => [
        ...prev,
        {
          id: Date.now() + Math.random(),
          x: 5 + Math.random() * 70, // spread across width (flows right via animation)
          y: 82 + Math.random() * 6,
          width: 70 + Math.random() * 70,
          pollutant,
        },
      ]);
    }, 1600); // slower generation

    return () => clearInterval(interval);
  }, [active, pollutants]);

  return (
    <>
      {streams.map((stream) => {
        const isHovered = hovered === stream.id;

        return (
          <div
            key={stream.id}
            className="absolute cursor-pointer"
            style={{
              left: `${stream.x}%`,
              top: `${stream.y}%`,
              zIndex: isHovered ? 150 : 2,
            }}
            onMouseEnter={() => setHovered(stream.id)}
            onMouseLeave={() => setHovered(null)}
          >
            {isHovered && <Tooltip pollutant={stream.pollutant} />}

            <div
              className="rounded-full animate-[flow_7s_linear_forwards] flex items-center justify-center text-lg font-black text-white distressed-text-normal"
              style={{
                width: stream.width,
                height: stream.width,
                background: stream.pollutant.color,
                opacity: 0.72,
              }}
            >
              {stream.pollutant.code}
            </div>
          </div>
        );
      })}

      <style jsx>{`
        @keyframes flow {
          from {
            transform: translateX(0px);
            opacity: 0.18;
          }
          to {
            transform: translateX(420px);
            opacity: 0;
          }
        }
      `}</style>
    </>
  );
}

