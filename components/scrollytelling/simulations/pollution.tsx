/* -----------------------------
   CONTROLS
------------------------------ */

function Controls({
  siteId,
  setSiteId,
  playing,
  setPlaying,
  layers,
  toggleLayer,
}: any) {
  const LAYER_CONFIG = {
    air: {
      icon: Wind,
      label: "Air",
    },
    soil: {
      icon: Mountain,
      label: "Soil",
    },
    water: {
      icon: Waves,
      label: "Water",
    },
  };

  return (
    <>
      {/* site type */}
      <div className="absolute top-6 left-0 right-0 flex flex-col items-center z-40">
        <div className="text-sm text-black/70 mb-3">Site Type</div>

        <div className="flex gap-3">
          {SITES.map((s) => {
            const active = siteId === s.id;

            return (
              <button
                key={s.id}
                onClick={() => setSiteId(s.id)}
                className={`px-5 py-2 rounded-full text-sm font-semibold transition-colors ${
                  active ? "bg-[#00A651]" : "bg-[#252A1E] hover:bg-[#343b2b]"
                }`}
              >
                {s.name}
              </button>
            );
          })}
        </div>
      </div>

      {/* play */}
      <div className="absolute top-28 left-0 right-0 flex justify-center z-40">
        <button
          onClick={() => setPlaying(!playing)}
          className="px-6 py-2.5 bg-black rounded-full text-sm font-semibold flex items-center gap-2"
        >
          {playing ? (
            <>
              <Pause className="w-4 h-4 fill-white" />
              Pause simulation
            </>
          ) : (
            <>
              <Play className="w-4 h-4 fill-white" />
              Run simulation
            </>
          )}
        </button>
      </div>

      {/* layers */}
      <div className="absolute left-5 top-40 z-50">
        <div className="bg-white/30 backdrop-blur-md rounded-2xl p-4 w-[150px] border border-white/10">
          <div className="text-sm text-black/60 mb-4">Environmental Layers</div>

          {(
            Object.entries(LAYER_CONFIG) as [
              LayerType,
              {
                icon: any;
                label: string;
              }
            ][]
          ).map(([layer, config]) => {
            const Icon = config.icon;

            return (
              <label
                key={layer}
                className="flex items-center gap-3 px-3 py-3 rounded-xl hover:bg-black/5 cursor-pointer transition-colors"
              >
                <button
                  type="button"
                  onClick={() => toggleLayer(layer)}
                  className={`w-5 h-5 shrink-0 rounded border flex items-center justify-center transition-all ${
                    layers[layer]
                      ? "bg-white/80 border-white"
                      : "border-white/40 bg-transparent"
                  }`}
                >
                  {layers[layer] && (
                    <Check className="w-3.5 h-3.5 text-black stroke-[3]" />
                  )}
                </button>

                <Icon className="w-4 h-4 text-black/80" />

                <span className="text-sm text-black">{config.label}</span>
              </label>
            );
          })}
        </div>
      </div>
    </>
  );
}

/* -----------------------------
   MAIN
------------------------------ */

export function PollutionSimulation() {
  const [siteId, setSiteId] = useState<SiteType>("refinery");
  const [resetKey, setResetKey] = useState(0);

  const [playing, setPlaying] = useState(false);

  const [layers, setLayers] = useState({
    air: true,
    soil: true,
    water: true,
  });

  const site = useMemo(() => SITES.find((s) => s.id === siteId)!, [siteId]);

  function toggleLayer(layer: LayerType) {
    setLayers((prev) => ({
      ...prev,
      [layer]: !prev[layer],
    }));
  }

  function handleSetSiteId(id: SiteType) {
    setSiteId(id);
    setResetKey((k) => k + 1);
  }

  return (
    <MapScene showSoil={layers.soil} showWater={layers.water}>
      <Facility site={site} />

      {layers.air && (
        <AirLayer
          active={playing}
          pollutants={site.pollutants.air}
          resetKey={resetKey}
        />
      )}

      {layers.soil && (
        <SoilLayer
          active={playing}
          pollutants={site.pollutants.soil}
          resetKey={resetKey}
        />
      )}

      {layers.water && (
        <WaterLayer
          active={playing}
          pollutants={site.pollutants.water}
          resetKey={resetKey}
        />
      )}

      <Controls
        siteId={siteId}
        setSiteId={handleSetSiteId}
        playing={playing}
        setPlaying={setPlaying}
        layers={layers}
        toggleLayer={toggleLayer}
      />

      {/* instructions */}
      <div className="absolute left-5 top-5 z-40 max-w-[280px] rounded-2xl bg-white/65 backdrop-blur-sm p-4 text-sm text-black shadow-xl">
        <p className="leading-relaxed opacity-80">
          Toggle environmental layers to see the pollutants generated by each
          site. Hover over pollutants to learn more about their impacts.
        </p>
      </div>
    </MapScene>
  );
}
